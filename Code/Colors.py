class Colors:

    __white = '\033[0m'
    __blue = '\033[34m'
    __red = '\033[31m'
    __green = '\033[32m'
    __orange = '\033[33m'
    __purple = '\033[35m'

    def white(self):
        return self.__white

    def blue(self):
        return self.__blue

    def green(self):
        return self.__green

    def red(self):
        return self.__red