class Colors:

    __white = '\033[0m'
    __blue = '\033[34m'
    __red = '\033[31m'
    __green = '\033[32m'
    __orange = '\033[33m'
    __purple = '\033[35m'

    def getWhite(self):
        return self.__white

    def getBlue(self):
        return self.__blue

    def getGreen(self):
        return self.__green

    def getRed(self):
        return self.__red